def apply_updates():
    if not os.path.exists(CONFIG_PATH):
        print(f"Error: {CONFIG_PATH} not found.")
        return

    updates = get_missing_keys_patch()
    
    with open(CONFIG_PATH, 'r', encoding='utf-8') as f:
        lines = f.readlines()
        
    new_lines = []
    current_lang = None
    
    for line in lines:
        stripped = line.strip()
        
        # Detect language block start
        # "af": {
        if stripped.startswith('"') and stripped.endswith('": {'):
            lang_code = stripped.strip('"').split('":')[0].strip('"')
            current_lang = lang_code
            new_lines.append(line)
            continue
            
        # Detect end of block (indentation change or closing brace)
        if stripped == '},' or stripped == '}':
            # Check if we have updates for this lang
            if current_lang in updates:
                # Add updates before closing brace
                for key, val in updates[current_lang].items():
                    # Format: "key": "value",
                    # Check if key already exists in the block to avoid dupe?
                    # The user said "missing translations", implying they might be missing OR present-but-english.
                    # My previous analysis showed they ARE present.
                    # So I should NOT append. I should REPLACE.
                    pass
            
            current_lang = None
            new_lines.append(line)
            continue
            
        # Inside block, check for keys to update
        if current_lang and current_lang in updates:
             # Basic regex or string split to find key
             # line looks like: "key": "value",
             if ':' in stripped:
                 parts = stripped.split(':', 1)
                 key_raw = parts[0].strip().strip('"')
                 if key_raw in updates[current_lang]:
                     # Replace line with new value
                     indent = line[:line.find('"')]
                     new_val = updates[current_lang][key_raw]
                     new_line = f'{indent}"{key_raw}": "{new_val}",\n'
                     new_lines.append(new_line)
                     # print(f"Updated {current_lang}.{key_raw}")
                     continue
        
        new_lines.append(line)

    # Write back
    with open(CONFIG_PATH, 'w', encoding='utf-8') as f:
        f.writelines(new_lines)
    print("Updates applied to translations.py")

if __name__ == "__main__":
    apply_updates()
